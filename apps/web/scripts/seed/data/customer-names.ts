/**
 * Generate a random French email from first and last name
 */
export function generateFrenchEmail(firstName: string, lastName: string): string {
  const domains = [
    'gmail.com', 'outlook.fr', 'orange.fr', 'free.fr', 'sfr.fr',
    'laposte.net', 'yahoo.fr', 'hotmail.fr', 'wanadoo.fr', 'icloud.com',
  ]

  const formats = [
    () => `${firstName.toLowerCase()}.${lastName.toLowerCase()}`,
    () => `${firstName.toLowerCase()}${lastName.toLowerCase()}`,
    () => `${firstName[0].toLowerCase()}.${lastName.toLowerCase()}`,
    () => `${firstName.toLowerCase()}.${lastName[0].toLowerCase()}`,
    () => `${lastName.toLowerCase()}.${firstName.toLowerCase()}`,
    () => `${firstName.toLowerCase()}${Math.floor(Math.random() * 100)}`,
  ]

  // Normalize for email (remove accents)
  const normalize = (str: string) =>
    str.normalize('NFD').replace(/[\u0300-\u036f]/g, '').replace(/[^a-zA-Z0-9.]/g, '')

  const format = formats[Math.floor(Math.random() * formats.length)]
  const domain = domains[Math.floor(Math.random() * domains.length)]

  return `${normalize(format())}@${domain}`
}

/**
 * Generate a company email
 */
export function generateCompanyEmail(companyName: string): string {
  const domains = ['entreprise.fr', 'societe.fr', 'pro.fr', 'asso.fr', 'org.fr']
  const domain = domains[Math.floor(Math.random() * domains.length)]

  // Clean company name for domain
  const cleanName = companyName
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-zA-Z0-9]/g, '')
    .toLowerCase()
    .slice(0, 20)

  const prefixes = ['contact', 'info', 'reservation', 'accueil', 'bonjour']
  const prefix = prefixes[Math.floor(Math.random() * prefixes.length)]

  return `${prefix}@${cleanName}.${domain}`
}
